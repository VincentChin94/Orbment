[RequireComponent(typeof(NavMeshAgent))]
[RequireComponent(typeof(DefenderVision))]
public class Chase : StateMachineBehaviour
{
    private NavMeshAgent m_agent;

    private DefenderVision m_defenderVision;
    // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        m_agent = animator.GetComponent<NavMeshAgent>();
        m_defenderVision = animator.GetComponent<DefenderVision>();
    }

    // OnStateUpdate is called on each Update frame between OnStateEnter and OnStateExit callbacks
    override public void OnStateUpdate(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        if(m_defenderVision.m_target != null)
        {
            m_agent.SetDestination(m_defenderVision.m_target.position);
        }
       

        animator.SetBool("inSight", m_defenderVision.inSight);
    }



